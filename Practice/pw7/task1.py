# Напишите функцию, которая заполняет файл
# (добавляет в конец) случайными парами чисел.
# Первое число int, второе - float разделены вертикальной чертой.
# Минимальное число - -1000, максимальное - +1000.
# Количество строк и имя файла передаются как аргументы функции.

import random

LOW = -1000
HIGH = 1000

def generate_random_pairs(filename, num_lines):
    with open(filename, 'a') as f:  # Открываем файл в режиме добавления ('a' - append)
        for _ in range(num_lines):
            int_num = random.randint(LOW, HIGH)   # Случайное целое число
            float_num = random.uniform(LOW, HIGH) # Случайное число с плавающей точкой
            f.write(f"{int_num} | {float_num:.2f}\n")  # Запись в файл с форматированием float

# Пример использования:
filename = "random_pairs.txt"
num_lines = int(input('Введите количество строк: '))  # Количество строк для добавления
generate_random_pairs(filename, num_lines)

# Объяснение:
# Импортируем модуль random:
# random.randint(-1000, 1000) — генерирует случайное целое число в диапазоне от -1000 до 1000.
# random.uniform(-1000, 1000) — генерирует случайное число с плавающей точкой в том же диапазоне.
# Функция generate_random_pairs(filename, num_lines):
# Открываем файл с именем filename в режиме добавления (используем флаг 'a').
# В цикле на каждую строку генерируем пару чисел: первое — целое, второе — вещественное, разделённые вертикальной чертой.
# f.write(f"{int_num} | {float_num:.2f}\n") — записываем строку в файл, где второе число форматируется до двух знаков после запятой с помощью :.2f.
